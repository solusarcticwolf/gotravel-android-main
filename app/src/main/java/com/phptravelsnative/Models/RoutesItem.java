package com.phptravelsnative.Models;

public class RoutesItem {
    String from ;
    String to ;

    public RoutesItem() {
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
